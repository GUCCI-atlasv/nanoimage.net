- 見知らぬコンピューターに保存されたくない画像

日常的な使用（休暇の写真のリサイズやSNS用の食べ物画像の圧縮など）ではリスクは低いです。しかし、デフォルトとしてブラウザベースのツールを使う習慣をつけることは合理的なアプローチです。

